/*
Método Filter Array

O método filter() cria uma cópia superficial de uma parte de uma determinada matriz, filtrada apenas para os elementos da matriz especificada que passam no teste implementado pela função fornecida.
*/

let arr = ['farooq', 'farooq', 'hi']

// filter 'hi' and 'farooq'
arr.filter(item => item == 'hi')